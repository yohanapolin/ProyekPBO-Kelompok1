public class Kasir {
    
    private String id;
    private String nama;
    private String laporan;

    public void setLaporan(String laporan) {
        this.laporan = laporan;
    }

    public String getLaporan() {
        return laporan;
    }

    public String getId() {
        return id;
    }

    public String getNama() {
        return nama;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    
    
}

